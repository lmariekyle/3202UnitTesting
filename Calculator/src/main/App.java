package main;

public class App {

    public App() {
    }

    public int add(int a, int b) {
        int result = a + b;

        return result;
    }

    public int subtraction(int a, int b) {
        int result = a - b;

        return result;
    }

    public int multiplication(int a, int b) {
        int result = a * b;

        return result;
    }

    public int division(int a, int b) {
        int result = a / b;

        return result;
    }

    public int modulo(int a, int b) {
        int result = a % b;

        return result;
    }

}
